SELECT ID, FISH_NAME, LENGTH
    FROM (SELECT *, MAX(LENGTH) OVER(PARTITION BY FISH_TYPE) AS MAX_LEN
            FROM FISH_INFO) I
    JOIN FISH_NAME_INFO N ON I.FISH_TYPE = N.FISH_TYPE
    WHERE LENGTH = MAX_LEN
    ORDER BY 1






















