SELECT H.FLAVOR
    FROM FIRST_HALF H
    JOIN JULY J
      ON H.FLAVOR = J.FLAVOR
    GROUP BY 1
    ORDER BY (H.TOTAL_ORDER + SUM(J.TOTAL_ORDER)) DESC
    LIMIT 3















